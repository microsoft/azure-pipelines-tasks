
'use strict';

var util = require('util');

var operations = require('./operations');
function ResourceManagementClient(credentials, resourceGroupName, subscriptionId, baseUri, options) {
  this.apiVersion = '2016-07-01';
  this.acceptLanguage = 'en-US';
  this.longRunningOperationRetryTimeout = 30;
  this.generateClientRequestId = true;
  if (credentials === null || credentials === undefined) {
    throw new Error('\'credentials\' cannot be null.');
  }
  if (subscriptionId === null || subscriptionId === undefined) {
    throw new Error('\'subscriptionId\' cannot be null.');
  }
  if (!options) options = {};

  this.baseUri = baseUri;
  if (!this.baseUri) {
    this.baseUri = 'https://management.azure.com';
  }
  this.credentials = credentials;
  this.subscriptionId = subscriptionId;

  if (options.apiVersion !== null && options.apiVersion !== undefined) {
    this.apiVersion = options.apiVersion;
  }
  if (options.acceptLanguage !== null && options.acceptLanguage !== undefined) {
    this.acceptLanguage = options.acceptLanguage;
  }
  if (options.longRunningOperationRetryTimeout !== null && options.longRunningOperationRetryTimeout !== undefined) {
    this.longRunningOperationRetryTimeout = options.longRunningOperationRetryTimeout;
  }
  if (options.generateClientRequestId !== null && options.generateClientRequestId !== undefined) {
    this.generateClientRequestId = options.generateClientRequestId;
  }
  this.deployments = new operations.ResourceGroupDeployments(this);
  this.resourceGroup = new operations.ResourceGroup(this);
  this.resourceGroupName = resourceGroupName;
}


module.exports = ResourceManagementClient;
