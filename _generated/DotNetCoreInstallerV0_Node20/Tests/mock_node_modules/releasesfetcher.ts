export class DotNetCoreReleaseFetcher {

    public static async getDownloadUrls(osSuffixes: string[], version: string, type: string) {
        if (process.env["__get_dlurls_failed__"] == "true") {
            throw "loc_mock_getDownloadUrlsFailed install-script failed to get donwload urls";
        }
        if (process.env["__primary_url_failed__"] == "true") {
            return ["https://primary-url", "https://legacy-url"];
        }

        if (type == "sdk" && version == "1.0.4") {
            return ["https://primary-url"];
        }
        if (type == "runtime") {
            return ["https://primary-runtime-url"];
        }
    }
}